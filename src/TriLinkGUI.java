    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        int parsedInt;
        try
        {
            parsedInt = Integer.parseInt(inputBox.getText());
            if(triTree.find(parsedInt))
            {
                jLabel2.setText("That number is on the list.");
            }
            else
            {
                jLabel2.setText("That number is not on the list.");
            }
        }
        catch (Exception ex)
        {
            // label box error: "bad input"
            jLabel2.setText("That is not a number in the tree. Try again.");
        }
           jTextArea1.setText(triTree.printTri() + triTree.CountNode());
           inputBox.requestFocus();
    }//GEN-LAST:event_buttonSearchActionPerformed

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        int parsedInt;
        try
        {
            parsedInt = Integer.parseInt(inputBox.getText());
            if(triTree.delete(parsedInt))
            {
                jLabel2.setText("Number has been deleted.");
            }
            else
            {
                jLabel2.setText("That number is not in the tree and cannot be deleted. Try again.");
            }
            inputBox.setText("");
        }
        catch (Exception ex)
        {
            // label box error: "bad input"
            jLabel2.setText("Bad input. Try again.");
            inputBox.selectAll();
        }
           jTextArea1.setText(triTree.printTri() + triTree.CountNode());
           inputBox.requestFocus();        // TODO add your handling code here:
    }//GEN-LAST:event_buttonDeleteActionPerformed

