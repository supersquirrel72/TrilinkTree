    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        //convert inputBox string to int.
        int parsedInt;
        try
        {
            parsedInt = Integer.parseInt(inputBox.getText());
            triTree.addNum(parsedInt);
            inputBox.setText("");
        }
        catch (Exception ex)
        {
            // label box error: "bad input"
            jLabel2.setText("Invalid input. Enter an integer.");
            inputBox.selectAll();
        }
           jTextArea1.setText(triTree.printTri() + triTree.CountNode());
           inputBox.requestFocus();
           
    }//GEN-LAST:event_buttonAddActionPerformed

